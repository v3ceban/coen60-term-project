                    <li class="projectTitle">Reverse Protein Engineering</li>
                        <ul>
                            <li>Reggie Antonio, Nick Deyarman, Robert Kempf, Andrew Zeng</li>
                            <li>Advisors: Zhiwen (Jonathan) Zhang, Liang Xiang</li>
                            <li class="projectDescription">Our goal is to reduce the size of an enzyme while
                                retaining its catalytic function. The approach
                                is to combinatorially screen designed peptide
                                libraries. If successful, this method could be
                                used for therapeutic enzymes and biosensors.</li>
                        </ul>
                    <li class="projectTitle">Pathogen Detection by Two-Dimensional Paper Networks</li>
                        <ul>
                            <li>Ian Nova, Samual Pontrelli, Miller Bauer</li>
                            <li>Business students: Luis Carillo, Marc Nguyen, Kirtan Patel</li>
                            <li>Advisor: Unyoung (Ashley) Kim</li>
                            <li class="projectDescription">The objective of our research is to develop
                                an inexpensive, automated, rapid pathogen
                                detection system without the use of electricity
                                or cold storage. We plan to achieve this goal
                                by using a two-dimensional paper network with
                                specific base pair recognition.</li>
                        </ul>
                    <li class="projectTitle">Detection of Waterborne Pathogens for the Developing World</li>
                        <ul>
                            <li>Jennifer Batara, Anusha Ravikumar</li>
                            <li>Advisor: Unyoung (Ashley) Kim</li>
                            <li class="projectDescription">One of the most relevant issues in
                                underdeveloped areas is the lack of access
                                to clean water. Our microfluidic device, which
                                utilizes a lysis chamber and an electrochemical
                                DNA sensor, can provide a reliable, fast, and
                                safe method for waterborne pathogen detection
                                in underdeveloped areas.
                            </li>
                        </ul>
                        <li class="projectTitle">Engineering a Cell as a Biosensor</li>
                        <ul>
                            <li>Alex Galan, Ryan Smith, Blake Williams, Alison Wuerstle</li>
                            <li>Advisors: Zhiwen (Jonathan) Zhang, Liang Xiang</li>
                            <li class="projectDescription">Our goal is to directly quantify protein
                                interactions in mammalian cells by using the
                                tetracycline repressor-based mammalian
                                two-hybrid system. If successful, this method
                                could be used for disease detection.</li>
                        </ul>
                    <li class="projectTitle">A Microfluidic Devide to Identify and Measure
                        Amine Concentrations in Cancer borealis</li>
                        <ul>
                            <li>Rian Draeger, Kira Kubota, Elizabeth Sugahara</li>
                            <li>Advisors:  Unyoung (Ashley) Kim, Steven Suljak</li>
                            <li class="projectDescription">The aim of our project is to fabricate a device
                                that specifically measures the concentrations
                                of dopamine, octopamine, and serotonin in
                                the Cancer borealis hemolymph. Ultimately
                                our analysis of amine concentrations in the
                                Cancer borealis will be applied to how amine
                                concentrations affect motor programming in humans.</li>
                        </ul>
                    <li class="projectTitle">Attempt to Increase the Adhesive Strength of
                        Cyanocrylate Glues Using Carbon Nanotubes</li>
                        <ul>
                            <li>Matt Blanco, Jens Olesen</li>
                            <li>Business students: Andrew Hoffman, Aska Odomo</li>
                            <li>Advisors: Prashanth Asuri, T. Kim Parnell</li>
                            <li class="projectDescription">Biological organisms in nature, such as
                                mussels and barnacles, are known to utilize
                                nanoparticles when adhering to surfaces. Using
                                this concept, we hope to improve the adhesive
                                strength of cyanoacrylate glues by adding
                                carbon nanotubes. If our assumption is correct,
                                our findings would guide the development of
                                stronger medical glues.</li>
                        </ul>
                        <li class="projectTitle">Interactive Model of Particle Flow throughBrain Tissue</li>
                        <ul>
                            <li>Kevin Tang, Camilo Taganas</li>
                            <li>Advisors: Prashanth Asuri, Maria Pantoja</li>
                            <li class="projectDescription">A manipulable MATLAB-generated model of
                                the brain that simulates the flow of particles
                                through brain tissue from an injection point to the
                                targeted area.</li>
                        </ul>
                    <li class="projectTitle">TheraPE</li>
                        <ul>
                            <li>Sofia Pour, Sarah van Keulen, Kristian Hockenson</li>
                            <li>Advisors: Paul Davison, Gerardo Noriega</li>
                            <li class="projectDescription">TheraPE is a minimally invasive pulmonary
                                embolectomy device. This catheter has both a
                                suction mechanism and a rotating rod with a
                                dynamic head for the fragmentation and total
                                removal of the blood clot. When combined,
                                these main functions create a device that is user
                                friendly, steerable, time efficient, and atraumatic.</li>
                        </ul>
                    <li class="projectTitle">Automated Utility Testing Operations (A.U.T.O.)</li>
                        <ul>
                            <li>John Fresquez, Zuhayr Elahi</li>
                            <li>Advisors: Raffi Attarian, Unyoung (Ashley) Kim</li>
                            <li class="projectDescription">The PEAK PlasmaBlade provides precise
                                tissue dissection with the bleeding control
                                of electrosurgery. During research and
                                development, PlasmaBlade prototypes
                                are tested on tissue models in a laboratory
                                environment. To facilitate control and automation
                                of performance testing, Medtronic Advanced
                                Energy tasked the team with developing a test
                                simulator for PlasmaBlade devices.
                            </li>
                        </ul>
